# Import the forms module from django to create forms based on our models
from django import forms
# Import the Issue model to base our form on, along with Book and Member just in case
from .models import Issue, Book, Member

# Create a ModelForm specifically for the Issue model
class IssueForm(forms.ModelForm):
    # The Meta class tells Django which model and fields to use for this form
    class Meta:
        # Link this form to the Issue model
        model = Issue
        # Specify that we only want the user to fill out the 'member' and 'due_date' fields
        fields = ['member', 'due_date']
        # Customize the default HTML widgets generated by Django
        widgets = {
            # Use an HTML5 date input type for the 'due_date' field, which renders a nice calendar picker
            'due_date': forms.DateInput(attrs={'type': 'date'}),
        }
